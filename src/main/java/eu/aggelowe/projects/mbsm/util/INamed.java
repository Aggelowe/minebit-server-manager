package eu.aggelowe.projects.mbsm.util;

/**
 * This class represents a new {@link Object} that has a specific name bound to
 * it.
 * 
 * @author Aggelowe
 *
 */
public interface INamed {

	/**
	 * This method should return the name of the {@link Object}.
	 * 
	 * @return The name of the object
	 */
	public String getName();

}
